#include "libft.h"

char	*ft_strcat(char *dst, char const *src)
{
	int				i;
	int				j;

	i = ft_strlen(dst);
	j = 0;
	while (src[j] != '\0')
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (dst);
}
