#include "libft.h"

char	*ft_strncpy(char *dst, char const *src, unsigned int n)
{
	unsigned int	i;
	unsigned char	ss1;

	ss1 = (unsigned char)*src;
	i = 0;
	while (i < n && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
