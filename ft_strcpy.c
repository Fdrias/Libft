#include "libft.h"

char	*ft_strcpy(char *dst, char const *src)
{
	int				i;
	unsigned char	src1;

	src1 = (unsigned char)*src;
	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
